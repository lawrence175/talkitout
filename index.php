drug and substance abuse, domestic violence and increased criminal activities.</code>Mental disorders are often stigmatized and exposed to negative judgements. Therefore, mental health patients have to deal with devastating outcomes resulting from the illness, and also social prejudice, isolation and exclusion. There is prevalent fear and lack of knowledge about mental illness among the general public resulting in the rising cases of suicide.
						</p>
